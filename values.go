package main

import "fmt"

func main(){

	//Addition for Strings
	fmt.Println("go" + "lang")
	
	//Addition for integers
	fmt.Println("1+1 =", 1+1)

	//Division for float numbers
	fmt.Println("7.0/3.0 =", 7.0/3.0)
	
	//True and false = false 
	fmt.Println(true && false)
	
	//True or false = true
	fmt.Println(true || false)

	//Not true = false
	fmt.Println(!true)
}

